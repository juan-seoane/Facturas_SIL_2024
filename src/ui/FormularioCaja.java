    public void limpiarFormulario(){
        this.txtCajaDia.setText("");
        this.txtCajaMes.setText("");
        this.txtCajaAnho.setText(Config.getConfig().getAnho().getAnho()+"");
        this.lblDate.setText(new GregorianCalendar().getTime()+"");
        this.txtCajaCantidad.setText("");
        /** TODO : AÑADIR A LA CONFIG EL TIPO DE RETENCION EN LOS INGRESOS */
        this.cmbOrigenCaja.setSelectedIndex(0);
        this.txtNota.setText("");
        
    }
    
    public void rellenarFormulario(Caja c){
        this.txtCajaDia.setText(c.getFecha().getDia()+"");
        this.txtCajaMes.setText(c.getFecha().getMes()+"");
        this.txtCajaAnho.setText(c.getFecha().getAnho()+"");
        this.txtCajaCantidad.setText(c.getCaja()+"");
        this.lblDate.setText(c.getDate()+"");
        if (c.isHaber()){
            btnHaber.setSelected(true);
            btnDebe.setSelected(false);
        } else {
            btnHaber.setSelected(false);
            btnDebe.setSelected(true);
        }
        /** TODO : AÑADIR A LA CONFIG EL TIPO DE RETENCION EN LOS INGRESOS */
        this.cmbOrigenCaja.setSelectedItem(c.getOrigen());
        this.txtNota.setText(c.getNota().getTexto());
    }
    public Caja recogerFormulario(){
        try{
        Caja nuevaCaja;
        int dia = Integer.parseInt(this.txtCajaDia.getText());
        int mes = Integer.parseInt(this.txtCajaMes.getText());
        int anho = Integer.parseInt(this.txtCajaAnho.getText());
        String origen = cmbOrigenCaja.getSelectedItem().toString();
        String nota = txtNota.getText();
        boolean haber;
        double cantidad = Double.parseDouble(this.txtCajaCantidad.getText());
        if (btnDebe.isSelected()){
            cantidad = cantidad;
            haber = false;
            nuevaCaja = new Caja(0,new Fecha(dia,mes,anho),new GregorianCalendar().getTime(), origen, cantidad , haber, new Nota(1,nota));
            return nuevaCaja;
        } 
        else if (btnHaber.isSelected()){
            cantidad = cantidad;
            haber = true;
           nuevaCaja = new Caja(0,new Fecha(dia,mes,anho),new GregorianCalendar().getTime(), origen, cantidad , haber, new Nota(1,nota)); 
           return nuevaCaja;
        }
        }catch (Exception ex){
            JOptionPane.showMessageDialog(null,"Ha habido un error en la recogida de datos!");
        }
        return null;
    }
