    public boolean nuevoConcepto(){
        String nuevo = JOptionPane.showInputDialog(null, "Introduzca el nuevo concepto:");
        if (!nuevo.trim().equals("")){
            this.cmbOrigenCaja.addItem(nuevo.toUpperCase().trim());
            ArrayList<Concepto> lista = Config.getConfig().getOrigenesCaja();
            lista.add(new Concepto(nuevo.toUpperCase().trim()));
            Config.getConfig().setOrigenesCaja(lista);
            if (Config.recConfig(Config.getConfig().getUsuario(), Config.getConfig()))
                return true;
            else return false;
        }
        JOptionPane.showMessageDialog(null,"El Concepto no se ha guardado!");
        return false;
    }
