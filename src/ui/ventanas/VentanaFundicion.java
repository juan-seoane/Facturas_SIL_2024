    private void btnOKVFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKVFActionPerformed
        trimestres = new ArrayList<Integer >();
        this.año = Integer.parseInt(txtAñoVF.getText());
        if (rbtntrim0VF.isSelected())
            trimestres.add(0);
        if (rbtntrim1VF.isSelected())
            trimestres.add(1);
        if (rbtntrim2VF.isSelected())
            trimestres.add(2);
        if (rbtntrim3VF.isSelected())
            trimestres.add(3);
        if (rbtntrim4VF.isSelected())
            trimestres.add(4);
            
        this.hecho = true;
    }//GEN-LAST:event_btnOKVFActionPerformed

