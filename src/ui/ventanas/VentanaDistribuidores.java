    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        int filsel = -1;
        NIF nifsel;
        
        filsel = this.tablaDist.getSelectedRow();
        String nifst = this.tablaDist.getModel().getValueAt(filsel, 0).toString();
        if (nifst.indexOf("-")<3)
        {    
            String[] nifArray = nifst.split("-");
            int nifnum = Integer.parseInt(""+nifArray[1]+nifArray[2]);
            nifsel = new NIF(nifnum, nifArray[0],true);
        }
        else{
           String[] nifArray = nifst.split("-");
            nifsel = new NIF(Integer.parseInt(nifArray[0]), nifArray[1],false); 
        }
        //JOptionPane.showMessageDialog(null, "El NIF seleccionado es "+nifsel.getNumero());
        String nombre = this.tablaDist.getModel().getValueAt(filsel, 1).toString().trim();

        for (RazonSocial rs : this.lista) {
//            //System.out.println("Comparando SelecciÃ³n : "+nifsel.toString());
//            //System.out.println("con Distribuidor : "+rs.getNIF().toString());
            if ((nifsel.toString()).equals(rs.getNIF().toString())&& nombre.trim().equals(rs.getNombre().trim())) {
                this.razonseleccionada = rs;
                this.seleccion = true;
                break;
            }
            else {
//                JOptionPane.showMessageDialog(null,"No se ha encontrado el distribuidor\n"+nifsel.toString()+" "+nombre+ "\nen la base de distribuidores");
            }
        }
    }//GEN-LAST:event_btnSeleccionarActionPerformed

