    public void cargarTabla(ArrayList<RazonSocial> listadistribuidores) {
        this.lista = listadistribuidores;
        Vector<Vector> datamodel = new Vector<Vector>();
        int i = -1;
        for (RazonSocial rs : listadistribuidores) {
            i++;
            Vector fila = new Vector();
            fila.add(((RazonSocial) listadistribuidores.get(i)).getNIF());
            fila.add(((RazonSocial) listadistribuidores.get(i)).getNombre());
            String filtro = txtBuscar.getText().toUpperCase();
            if (filtro.equals(""))
                datamodel.add(fila);
            else if ((listadistribuidores.get(i).getNIF().getNumero()+"").toUpperCase().contains(filtro) || (listadistribuidores.get(i).getNIF().getLetra()+"").toUpperCase().contains(filtro) || listadistribuidores.get(i).getNombre().toUpperCase().contains(filtro) || listadistribuidores.get(i).getRazon().toUpperCase().contains(filtro)){
                datamodel.add(fila);
            }
        }
        Vector colnames = new Vector();
        colnames.add("NIF");
        colnames.add("NOMBRE");
        DefaultTableModel modeloDist = new DefaultTableModel(datamodel, colnames);
        tablaDist.setModel(modeloDist);
    }

