    public void rellenarTabla(Vector<Vector> datamodel, Vector colnames) {
        DefaultTableModel modelo = new DefaultTableModel(datamodel, colnames);
        tablaDistribuidores.setModel(modelo);

    }
    
    public void actualizarModelo(Vector distribuidores, int sel ){
        DefaultTableModel modelo = new DefaultTableModel(distribuidores,ModeloDistribuidores.getColumnas());
            
        tablaDistribuidores.setModel(modelo);
        
        ListSelectionModel selectionModel = tablaDistribuidores.getSelectionModel();
	selectionModel.setSelectionInterval(sel, sel);
        
    }
    
    public void filaSeleccionada() {
        
        ListSelectionModel selectionModel = tablaDistribuidores.getSelectionModel();
        indice = selectionModel.getLeadSelectionIndex();
//        JOptionPane.showMessageDialog(null,"Fila seleccionada  : " + (indice+1));
        if (indice <0)
            indice = 0;
//        hacambiado = true;
//        pulsado = 0;
    }
    
